#!/usr/bin/env python3

"""
Module: arithmetic_operations

Description:
    This module provides basic arithmetic operations such as addition,
    subtraction, multiplication, and division. Each function takes
    numerical inputs and returns the corresponding results.
"""


def add(a: float, b: float) -> float:
    """
    Description:
        Adds two floating point numbers.

    Parameters:
        a (float): The first number to be added.
        b (float): The second number to be added.

    Returns:
        float: The sum of the two input numbers.
    """
    return a + b
